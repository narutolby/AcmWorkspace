#include<stdio.h>
int main(){
 int time;
 scanf("%d",&time);
 if(time<=120 && time>60){
   printf("%.3f\n",0.8);
 }else if(time<=60 && time>45){
   printf("%.3f\n",0.6);
 }else if(time<=45 && time>30){
   printf("%.3f\n",0.6*0.6);
 }else if(time<=30 && time>15){
   printf("%.3f\n",0.6*0.6*0.6);
 }else if(time<=15){
   printf("%.3f\n",0.6*0.6*0.6*0.6);
 }
 return 0;
}
